package com.dambrevilleguyen.easybudget.model;

public class BudgetHandler {
	
	private int money;
	private int pocketMoney;
	private int groceryShopping;
	private int shopping;
	private int hobbies;
	private int trips;
	
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public int getPocketMoney() {
		return pocketMoney;
	}
	public void setPocketMoney(int pocketMoney) {
		this.pocketMoney = pocketMoney;
	}
	public int getGroceryShopping() {
		return groceryShopping;
	}
	public void setGroceryShopping(int groceryShopping) {
		this.groceryShopping = groceryShopping;
	}
	public int getShopping() {
		return shopping;
	}
	public void setShopping(int shopping) {
		this.shopping = shopping;
	}
	public int getHobbies() {
		return hobbies;
	}
	public void setHobbies(int hobbies) {
		this.hobbies = hobbies;
	}
	public int getTrips() {
		return trips;
	}
	public void setTrips(int trips) {
		this.trips = trips;
	}

}
